const tester = require('./logistics.js');

describe('logistics test', function() {
    it('input test #1', () => {
        const a = tester([
            [0, 4, 1],
            [4, 1, 2],
            [4, 3, 2],
            [4, 6, 6],
            [5, 5, 2],
            [0, 5, 5],
            [0, 2, 5],
            [4, 4, 2],
            [2, 1, 4],
            [6, 1, 2],
            [0, 2, 2],
            [6, 6, 0]
        ])
        expect(a).toStrictEqual([1, 2, 3, 6, 7, 8, 9, 11, 12, 13, 14, 21]);
    })
    it('input test #2', () => {
        const a = tester([
            [5, 4, 1],
            [2, 1, 2],
            [2, 3, 2],
            [3, 6, 6],
            [4, 5, 2],
            [6, 5, 5],
            [1, 2, 5],
            [0, 4, 2],
            [3, 1, 4],
            [4, 1, 2],
            [5, 2, 2],
            [6, 6, 0]
        ])
        expect(a).toStrictEqual([1, 2, 3, 4, 6, 7, 8, 9, 11, 12, 13, 14, 20, 21]);
    })
    it('input test #3', () => {
        const a = tester([
            [5, 4, 5],
            [2, 1, 4],
            [2, 3, 4],
            [3, 6, 2],
            [4, 5, 1],
            [6, 5, 0],
            [1, 2, 6],
            [0, 4, 3],
            [3, 1, 5],
            [4, 1, 2],
            [5, 2, 2],
            [6, 6, 1]
        ])
        expect(a).toStrictEqual([1, 2, 3, 4, 6, 7, 8, 9, 10, 11, 12, 13, 14, 19, 20])
    })
    it('input test #4', () => {
        const a = tester([
            [5, 0, 5],
            [2, 0, 4],
            [2, 5, 4],
            [3, 1, 2],
            [4, 2, 1],
            [6, 2, 0],
            [1, 4, 6],
            [0, 3, 3],
            [3, 5, 5],
            [4, 5, 2],
            [5, 4, 2],
            [6, 0, 1]
        ])
        expect(a).toStrictEqual([0, 1, 3, 4, 6, 8, 9, 10, 11, 12, 13, 14, 18, 19])
    })
})